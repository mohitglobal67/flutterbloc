  String? title;
  String? image;
  String? count;

  Category({
    this.catId,
    this.title,
    this.image,
    this.count,
  });

  Category copyWith({
    String? catId,
    String? title,
    String? image,
    String? count,
  }) =>
      Category(
        catId: catId ?? this.catId,
        title: title ?? this.title,
        image: image ?? this.image,
        count: count ?? this.count,
      );

  factory Category.fromJson(Map<String, dynamic> json) => Category(
        catId: json["catId"],
        title: json["title"],
        image: json["image"],
        count: json["count"],
      );

  Map<String, dynamic> toJson() => {
        "catId": catId,
        "title": title,
        "image": image,
        "count": count,
      };
}
